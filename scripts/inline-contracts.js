import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const distDir = path.resolve('dist');
const contractsDir = path.join(distDir, 'contracts');

async function inlineContracts() {
	const entries = await readdir(contractsDir, { withFileTypes: true });
	const contractFiles = entries
		.filter((entry) => entry.isFile() && entry.name.endsWith('.es'))
		.map((entry) => entry.name);

	if (contractFiles.length === 0) {
		return;
	}

	for (const fileName of contractFiles) {
		const filePath = path.join(contractsDir, fileName);
		const content = await readFile(filePath, 'utf8');
		const jsPath = path.join(contractsDir, `${fileName}.js`);
		const jsModule = `// Auto-generated by scripts/inline-contracts.js\nexport default ${JSON.stringify(content)};\n`;
		await writeFile(jsPath, jsModule, 'utf8');
	}

	const distTargets = ['index.js', 'envs.js'];
	for (const target of distTargets) {
		const targetPath = path.join(distDir, target);
		const source = await readFile(targetPath, 'utf8');
		const updated = source
			.replace(/\.es\?raw\b/g, '.es.js')
			.replace(/\.es(?!\.js)\b/g, '.es.js');
		if (updated !== source) {
			await writeFile(targetPath, updated, 'utf8');
		}
	}

	const indexTypesPath = path.join(distDir, 'index.d.ts');
	const indexTypes = await readFile(indexTypesPath, 'utf8');
	let updatedTypes = indexTypes
		.replace(/export\s+\{\s+default\s+as\s+reputation_proof_contract\s+\}\s+from\s+['"].*reputation_proof\.es(\?raw)?['"];\n?/g, 'export const reputation_proof_contract: string;\n')
		.replace(/export\s+\{\s+default\s+as\s+digital_public_good\s+\}\s+from\s+['"].*digital_public_good\.es(\?raw)?['"];\n?/g, 'export const digital_public_good: string;\n');

	if (updatedTypes !== indexTypes) {
		await writeFile(indexTypesPath, updatedTypes, 'utf8');
	}
}

inlineContracts().catch((error) => {
	console.error('Failed to inline .es contracts:', error);
	process.exit(1);
});
